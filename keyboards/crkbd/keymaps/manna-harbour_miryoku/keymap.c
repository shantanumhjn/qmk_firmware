// this has been copied over from users\manna-harbour_miryoku\miryoku_babel\miryoku_layer_list.h
enum layers { BASE, EXTRA, TAP, BUTTON, NAV, MOUSE, MEDIA, NUM, SYM, FUN };

void oled_render_layer_state(void) {
  oled_write("Layer: ", false);

  switch (get_highest_layer(default_layer_state|layer_state)) {
  case BASE:
    oled_write_ln("Mac", false);
    break;
  case BUTTON:
    oled_write_ln("Button", false);
    break;
  case MEDIA:
    oled_write_ln("Media", false);
    break;
  case NAV:
    oled_write_ln("Navigation", false);
    break;
  case MOUSE:
    oled_write_ln("Mouse", false);
    break;
  case SYM:
    oled_write_ln("Symbol", false);
    break;
  case NUM:
    oled_write_ln("Number", false);
    break;
  case FUN:
    oled_write_ln("Function", false);
    break;
  case EXTRA:
    oled_write_ln("Windows", false);
    break;
  case TAP:
    oled_write_ln("Qwerty", false);
    break;
  }
}

void oled_render_led_state(void) {
  led_t led_state = host_keyboard_led_state();

  oled_write(led_state.caps_lock ? "CAPS " : "      ", false);
  oled_write(led_state.scroll_lock ? "SCROLL " : "       ", false);
  oled_write("\n", false);
}

void oled_render_mod_state(void) {
  uint8_t mod_state = get_mods();

  oled_write((mod_state & MOD_MASK_SHIFT) ? "SHIFT " : "      ", false);
  oled_write((mod_state & MOD_MASK_CTRL) ? "CTRL " : "     ", false);
  oled_write((mod_state & MOD_MASK_ALT) ? "ALT " : "    ", false);
  oled_write((mod_state & MOD_MASK_GUI) ? "GUI" : "   ", false);
  oled_write("\n", false);
}

